package edu.robert.usofinal;
public class Final {

    public static void main(String[] args) {
        
    
    final String BR = "Brasil";   // o modificador 'final' define que esse valor 'BR' não pode ser alterado posteriormente.

    BR = "Brazil";

    int ano = 2021;               // nesse caso o valor 'ano' pode ser modificado pois não está acompanhado por um 'final'.

    ano = 2022;
    
}
}
